// ------------------- Загрузка картинок и фреймов анимации ------------------- 
// Данный пример работает при размере матрицы 16x16 - такие картинки подготовлены в этом файле
// для других размеров матрицы - подготовьте собственные картинки

timerMinim gifTimer(D_GIF_SPEED);

void loadImage(const uint16_t (*frame)) {
  int8_t offset_x = (WIDTH - FRAME_WIDTH) / 2;
  int8_t offset_y = (HEIGHT - FRAME_HEIGHT) / 2;
  for (byte i = 0; i < FRAME_WIDTH; i++) {
    if (offset_x + i < 0 || offset_x + i > WIDTH - 1) continue;
    for (byte j = 0; j < FRAME_HEIGHT; j++) {
      if (offset_y + j < 0  || offset_y + j > HEIGHT - 1) continue;
      drawPixelXY(offset_x + i, offset_y + j, gammaCorrection(expandColor((pgm_read_word(&(frame[(HEIGHT - j - 1) * WIDTH + i]))))));
    }
  }  
  // да, тут происходит лютенький п@здец, а именно:
  // 1) pgm_read_word - восстанавливаем из PROGMEM (флэш памяти) цвет пикселя в 16 битном формате по его координатам
  // 2) expandColor - расширяем цвет до 24 бит (спасибо adafruit)
  // 3) gammaCorrection - проводим коррекцию цвета для более корректного отображения
}

// показать картинку
void imageRoutine() {
  if (loadingFlag) {
    loadingFlag = false;
    modeCode = MC_IMAGE;
    loadImage(frame00);
  }
}

void animation() {
  if (loadingFlag) {
    loadingFlag = false;
    modeCode = MC_IMAGE;
  }
  bool isReady = gifTimer.isReady();
  if (isReady) {
    frameNum++;
    if (frameNum >= 4) frameNum = 0;
    switch (frameNum) {
      case 0:
        loadImage(frame00);
        break;
      case 1:
        loadImage(frame01);
        break;
      case 2:
        loadImage(frame02);
        break;
      case 3:
        loadImage(frame03);
        break;
    }
  }  
}
